package action;

/**
 * ActionStatus determines the related Action
 *
 * @author jordannathanael
 * @version JDK 19
 */
public enum ActionStatus {
    /**
     * Place Action
     */
    PLACE,
    /**
     * Move Action
     */
    MOVE,
    /**
     * Fly Action
     */
    FLY,
    /**
     * Remove Action
     */
    REMOVE
}
